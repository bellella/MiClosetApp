function generateColorVars(
  tokens: typeof colorTokens.light | typeof colorTokens.dark
) {
  const result: Record<string, string> = {};

  for (const [colorName, colorValues] of Object.entries(tokens)) {
    for (const [level, value] of Object.entries(colorValues)) {
      result[`--color-${colorName}-${level}`] = value as string;
    }
  }

  return result;
}

